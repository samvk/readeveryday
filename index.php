		<article id="summer-slide">
			<div class="container">
				<h2>What is "Summer Slide"?</h2>
				<hr>
				<p>
					The reasons why we believe so strongly in consistent reading in the summer have everything to do with the research regarding <span class="loud">“summer slide”</span> and academic progress. In a nutshell, students who read during the summer maintain the learning that they developed during the course of the previous school year. The research is also clear about the effects of students not reading over the summer:
				</p>
				<ul>
					<li>Students can lose up to 25% of their reading level over the summer.</li>
					<li>Students can lose up to three months of their education over the summer.</li>
					<li>This loss is cumulative –so by the time a student enters middle school or high school, he or she could be years behind grade-level standards.
					</li>
				</ul>
				<p>For more information visit visit <a href="http://www.summerlearning.org/?page=know_the_facts">Summer Learning: Know the Facts</a> or <a href="http://scholar.harvard.edu/jameskim/publications/how-make-summer-reading-effective">How to Make Summer Reading Effective</a>.</p>
			</div>
		</article>

