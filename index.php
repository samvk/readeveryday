				</p>
				<p>
					Try setting up a plan for reading (which days, time of day, location)—and stick to it!
				</p>
				<ul>
					<li>For reluctant readers: you might want to avoid their bedrooms or other places that they associate with sleeping.
					</li>
					<li>For readers who do well with routine: choosing a consistent place to read might be best.
					</li>
				</ul>
